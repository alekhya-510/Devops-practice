def addition(num1,num2):
    add = num1 + num2
    return add
def subtraction(num1,num2):
    sub = num1 - num2
    return sub
def mul(num1,num2):
    m = num1 * num2
    return m
print("Addition is :", addition(15,20))
print("Subtraction is :", subtraction(60,15))
print("Multiplication is :", mul(4,10))